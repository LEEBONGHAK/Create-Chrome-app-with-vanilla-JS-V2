// const and let
/*
    const - 변수의 값을 바꿀 수 없음, 일반적으로 사용
    let - 변수의 값을 바꿀 수 있음, 필요에 따라 사용
    var - use in old version JS, 위의 두 선언법을 쓰는 편이 더 좋음
*/ 
let c = 5;
let name = "Kim";

console.log(c);
console.log("hello " + name);

c = 7;
name = "Lee";

console.log(c);
console.log("hello " + name);
console.log();

